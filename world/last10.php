// a list of the last ten days of shows

define("TENDAYS", (10*24*60*60)); // number of seconds in 10 days

/* always include this first */
include("config.inc.php");

/* we are going to use the ShowList class */
import("org/wprb/ShowList.php");
$today = time();
$start = $today - TENDAYS;
$s_list = new ShowList($start, $today);
$s_list->reset();

?>
