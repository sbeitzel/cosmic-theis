?>
<p>&nbsp;
<h2>List of shows for the last 10 days</h2>
<!-- ********** END REMOVABLE ********** -->

<div class='text'>
<table cellpadding="5">
<?php
while ( $s_list->hasNext() )	{
	$row = $s_list->next();
	$day = getdate($row->starttime);
	// shade color depends on day of week
	($day[wday]%2) ? $bgcol="#F0F0F0" : $bgcol = "#EEEEFF";
	print "\t<tr>\n\t\t<td class='mid' width='60%' bgcolor='$bgcol'>";
	print "<a href=\"printplaylist.php?show_id=$row->ID\">";
	if (! empty($row->title) )
		print "$row->title";
	else print "$row->othergenre";
	print " with $row->djname</a></td>\n";
	print "\t\t<td class='mid' bgcolor='$bgcol'>".date("l, F j, Y, H:i", $row->starttime)." - ";
	print date("H:i", ($row->starttime+$row->duration*60*60))."</td>\n";
	print "\t</tr>\n";
}

